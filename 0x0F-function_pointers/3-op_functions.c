/**
 * op_add - sums and b
 * @a:integer
 * @b:integer
 *
 * Return: Sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_add - finds the difference
 * @a:integer
 * @b:integer
 *
 * Return: Difference 
 */

int op_sub(int a, int b)
{
	return (a - b);
}

int op_mul(int a, int b)
{
	return (a * b);
}

int op_div(int a, int b)
{
	return (a / b);
}

int op_mod(int a, int b)
{
	return (a % b);
}
