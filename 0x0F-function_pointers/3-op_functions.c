/**
 * op_add - finds the sum
 * @a:integer
 * @b:integer
 *
 * Return: Sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - finds the difference
 * @a:integer
 * @b:integer
 *
 * Return: Difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - finds the product
 * @a: Integer
 * @b: Integer
 *
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - finds the dividend
 * @a: Integer
 * @b: Integer
 *
 * Return: Dividend
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - finds remainder after division
 * @a: Integer
 * @b: Intger
 *
 * Return: Remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
