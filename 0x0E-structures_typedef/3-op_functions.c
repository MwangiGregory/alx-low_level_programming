/**
 * op_add - finds sum
 * @a: Integer
 * @b: Integer
 *
 * Return: Sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - finds difference
 * @a: Integer
 * @b: Integer
 *
 * Return: Difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - finds product
 * @a: integer
 * @b: Integer
 *
 * Return: Product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - finds dividend
 * @a: integer
 * @b: integer
 *
 * Return: dividend after division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - finds remainder
 * @a: integer
 * @b: integer
 *
 * Return: remainder after division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
